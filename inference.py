def _extract_json(text: str) -> str:
    """Robustly extract a JSON object from model output.


    Handles markdown fences, leading prose, and trailing text by
    locating the outermost ``{...}`` block.
    """
    # Strip markdown code fences first
    if "```" in text:
        parts = text.split("```")
        for part in parts:
            stripped = part.strip()
            if stripped.startswith("json"):
                stripped = stripped[4:].strip()
            if stripped.startswith("{"):
                text = stripped
                break


    # Fallback: find the first '{' and last '}'
    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end != -1 and end > start:
        text = text[start:end + 1]


    return text.strip()




# Generated by GitHub Copilot
def _call_llm(prompt_content: str) -> tuple:
    """Call the LLM with retry logic.


    Returns:
        tuple: (CodeReviewAction, error_string)
    """
    max_retries = 3
    for attempt in range(max_retries):
        try:
            response = client.chat.completions.create(
                model=MODEL_NAME,
                messages=[{"role": "user", "content": prompt_content}],
                temperature=0.0,
                max_tokens=2048,
            )
            raw = response.choices[0].message.content.strip()
            raw = _extract_json(raw)
            data = json.loads(raw)
            issues = [
                IssueReport(
                    file=i.get("file", ""),
                    line=int(i.get("line", 0)),
                    issue_type=i.get("issue_type", "bug"),
                    description=i.get("description", ""),
                )
                for i in data.get("issues", [])
            ]
            return (
                CodeReviewAction(
                    issues=issues,
                    summary=data.get("summary", ""),
                ),
                "null",
            )
        except json.JSONDecodeError as e:
            if attempt == max_retries - 1:
                return (
                    CodeReviewAction(issues=[], summary="parse error"),
                    f"json_parse_error: {str(e)[:80]}",
                )
        except Exception as e:
            if attempt == max_retries - 1:
                return (
                    CodeReviewAction(issues=[], summary="api error"),
                    f"api_error: {str(e)[:80]}",
                )
    return (
        CodeReviewAction(issues=[], summary="max retries"),
        "max_retries_exceeded",
    )




