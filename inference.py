def run_episode(task_id: str) -> dict:
    """Run a full multi-step episode for a single task.


    The agent gets up to MAX_EPISODE_STEPS attempts.  After each
    non-final attempt the environment returns feedback that the agent
    uses to refine its review.
    """
    env = CodeReviewEnvironment()
    obs = env.reset(task_id)


    # ── [START] ──────────────────────────────────────────────────────────────
    print(f"[START] task={task_id} env={BENCHMARK} model={MODEL_NAME}", flush=True)


    step_num    = 0
    all_rewards = []
    score       = 0.0
    done        = False
    result_meta = {}


    while not done and step_num < MAX_EPISODE_STEPS:
        # Build prompt: initial on first step, refinement thereafter
        if step_num == 0:
            prompt_content = build_prompt(obs)
        else:
            prompt_content = build_refinement_prompt(obs)


        # Call LLM with retry logic
        action, error = _call_llm(prompt_content)


        # Step the environment
        obs = env.step(action)
        step_num += 1
        reward = obs.reward
        done = obs.done
        all_rewards.append(reward)
        score = reward
        result_meta = getattr(obs, "metadata", {}) or {}


        # ── [STEP] ───────────────────────────────────────────────────────────
        action_str = f"issues={len(action.issues)}"
        print(
            f"[STEP] step={step_num} action={action_str} "
            f"reward={reward:.3f} done={str(done).lower()} error={error}",
            flush=True,
        )


    success = score > 0.001


    # ── [END] ────────────────────────────────────────────────────────────────
    rewards_str = ",".join(f"{r:.3f}" for r in all_rewards)
    print(
        f"[END] success={str(success).lower()} steps={step_num} "
        f"score={score:.3f} rewards={rewards_str}",
        flush=True,
    )


    return {
        "task_id": task_id,
        "score": score,
        "found": result_meta.get("found", []),
        "missed": result_meta.get("missed", []),
    }




